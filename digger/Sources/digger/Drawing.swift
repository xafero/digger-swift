    func outtext(_ pConst: String, _ xConst: Int, _ yConst: Int, _ cConst: Int, _ bConst: Bool) {
        var p = pConst
        var x = xConst
        var y = yConst
        var c = cConst
        var b = bConst

        var i: Int = 0
        var rx: Int = x

        i = 0
        while i < p.length() {
            let letter = p.charAt(i)
            dig.Pc!.gwrite(x, y, letter, c)
            x += 12

            i += 1
        }

        if b {
            dig.Pc!.currentSource.newPixels(rx, y, p.length() * 12, 12)
        }
    }

