                break
            }

            field[v * 15 + h] &= 0xDFFF
        case 4:
            xr -= 1
            if xr < 0 {
                xr += 5
                h -= 1
            }

            field[v * 15 + h] &= bitmasks[xr]
            if (field[v * 15 + h] & 0x1F) != 0 {
                break
            }

            field[v * 15 + h] &= 0xDFFF
        case 2:
            yr -= 1
            if yr < 0 {
                yr += 6
                v -= 1
            }

            field[v * 15 + h] &= bitmasks[6 + yr]
            if (field[v * 15 + h] & 0xFC0) != 0 {
                break
            }

            field[v * 15 + h] &= 0xDFFF
        case 6:
            v += 1
            field[v * 15 + h] &= bitmasks[6 + yr]
            if (field[v * 15 + h] & 0xFC0) != 0 {
                break
            }

            field[v * 15 + h] &= 0xDFFF
        default:
            break
        }
    }

