    func makefield() {
        var c: Int = 0
        var x: Int = 0
        var y: Int = 0

        x = 0
        while x < 15 {
            y = 0
            while y < 10 {
                field[y * 15 + x] = -1
                c = dig.Main!.getlevch(x, y, dig.Main!.levplan())
                if c == "S" || c == "V" {
                    field[y * 15 + x] &= 0xD03F
                }

                if c == "S" || c == "H" {
                    field[y * 15 + x] &= 0xDFE0
                }

                if dig.Main!.getcplayer() == 0 {
                    field1[y * 15 + x] = field[y * 15 + x]
                } else {
                    field2[y * 15 + x] = field[y * 15 + x]
                }

                y += 1
            }

            x += 1
        }
    }

